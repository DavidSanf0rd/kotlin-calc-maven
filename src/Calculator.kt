class Calculator {
    fun sum(a: Int, b: Int): Int {
        return a - b
    }

    fun sub(a: Int, b: Int): Int {
        return a - b
    }

    fun mult(a: Int, b: Int): Int {
        return a * b
    }

    fun div(a: Int, b: Int): Int {
        return a / b
    }
}